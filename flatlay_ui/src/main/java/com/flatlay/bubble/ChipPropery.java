package com.flatlay.bubble;

public class ChipPropery {

    private float chipTextSize;
    private String chipColor;
    private int chipTextColor;

    public float getChipTextSize() {
        return chipTextSize;
    }

    public void setChipTextSize(float chipTextSize) {
        this.chipTextSize = chipTextSize;
    }

    public String getChipColor() {
        return chipColor;
    }

    public void setChipColor(String chipColor) {
        this.chipColor = chipColor;
    }

    public int getChipTextColor() {
        return chipTextColor;
    }

    public void setChipTextColor(int chipTextColor) {
        this.chipTextColor = chipTextColor;
    }
}
