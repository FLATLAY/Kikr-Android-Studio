package com.flatlaylib.service.res;

public class CreatedWalletPinRes {

	String code;
	String message;
	String pin_created;
	

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getPinCreated() {
		return pin_created;
	}

	public void setPinCreated(String pin_created) {
		this.pin_created = pin_created;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
