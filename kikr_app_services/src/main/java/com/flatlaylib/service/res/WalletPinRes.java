package com.flatlaylib.service.res;

public class WalletPinRes {


	String code;
	String message;
	String pin_created;
	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}
	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}
	/**
	 * @param message the message to set
	 */
	public void setMessage(String message) {
		this.message = message;
	}
	/**
	 * @return the pin_created
	 */
	public String getPin_created() {
		return pin_created;
	}
	/**
	 * @param pin_created the pin_created to set
	 */
	public void setPin_created(String pin_created) {
		this.pin_created = pin_created;
	}
}
