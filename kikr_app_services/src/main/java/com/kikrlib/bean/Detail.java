package com.kikrlib.bean;

public class Detail {
	String collection_id_reference;
	String type;
	String date;
	String reason;
	String amount;

	public String getCollection_id_reference() {
		return collection_id_reference;
	}

	public void setCollection_id_reference(String collection_id_reference) {
		this.collection_id_reference = collection_id_reference;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

}
