package com.kikrlib.bean;

public class Store {
	String id;
	String name;
	String img;
	String is_followed;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the img
	 */
	public String getImg() {
		return img;
	}

	/**
	 * @param img
	 *            the img to set
	 */
	public void setImg(String img) {
		this.img = img;
	}

	/**
	 * @return the is_followed
	 */
	public String getIs_followed() {
		return is_followed;
	}

	/**
	 * @param is_followed
	 *            the is_followed to set
	 */
	public void setIs_followed(String is_followed) {
		this.is_followed = is_followed;
	}

}
